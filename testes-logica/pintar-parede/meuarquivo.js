let width = Number(prompt("Largura da parede"))
let height = Number(prompt("Altura da parede"))
alert(`Sua parede tem dimensão de ${width}x${height}`)
let area = width*height
alert(`Área de ${area}m²`)
let check = area/2
alert(`Logo o quantidade de tinta necessária para pintar essa parede é de ${check}L de tinta`)